#ifndef _TEAM_H
#define _TEAM_H

/**
 * \brief An enum class to have a value for each possible team.
 */
enum class TEAM
{
    /** \brief Blue team member starting with index 0 */
    BLUE = 0,
    /** \brief Red team member */
    RED,
    /** \brief Green team member */
    GREEN,
    /** \brief Yellow team member */
    YELLOW,
};

#endif
